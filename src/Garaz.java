public class Garaz {
    private final int powierzchnia;
    private final String opis;

    public Garaz(int powierzchnia, String opis) {
        this.powierzchnia = powierzchnia;
        this.opis = opis;
    }

    public int getPowierzchnia() {
        return powierzchnia;
    }

    public String getOpis() {
        return opis;
    }

}